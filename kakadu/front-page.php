                <h2 class="article__title padding-left">
                    Saiba como a atividade física e o cuidado com a saúde podem
                    ser seus melhores investimentos para 2021
                </h2>
                <div class="content__text">
                    <figure class="full-image">
                        <img src="assets/images/full-img-1.jpg" alt="" />
                    </figure>
                    <p>
                        O ano de 2020 foi desafiador em muitos aspectos. Com os
                        efeitos da pandemia no mercado de trabalho e a mudança
                        para o modelo de home office, o estresse nos acompanhou
                        o ano todo. A boa notícia é que se livrar desse
                        companheiro indesejado é mais fácil do que se imagina.
                    </p>

                    <p>
                        Simples atitudes podem garantir um 2021 mais leve para
                        todos. A prática de atividade física, por exemplo, traz
                        benefícios que vão além do corpo, contribuindo também
                        com nossos resultados no trabalho.
                    </p>

                    <p>
                        <b
                            >“A atividade física melhora os níveis de hormônios
                            de bem-estar, como as beta endorfinas e serotonina,
                            ajudando no combate à depressão e ansiedade, além de
                            melhorar a cognição e concentração. E, como tem
                            impacto no sono, aumenta a disposição do dia
                            seguinte também”, explica Ricardo Corsi Sanchez, Cardiologista especialista pela Sociedade
                            Brasileira de Cardiologia.
                        </b>
                    </p>

                    <p>
                        Quanto melhor nossa saúde estiver, maior será nossa
                        performance. Logo, o ideal é combinar os exercícios com
                        uma alimentação de qualidade, nutritiva e balanceada. No
                        almoço, aposte em uma refeição leve, o que permite uma
                        digestão mais rápida e evita a sonolência após o almoço.
                        É importante também priorizar alimentos frescos aos
                        industrializados, apostar na variedade, evitar frituras,
                        doces, sal em excesso, e não esquecer de manter a
                        hidratação em dia.
                    </p>

                    <p>
                        A união desses hábitos impacta diretamente na diminuição
                        da tensão e aumento na capacidade de aprendizagem, além
                        de fortalecer o sistema imunológico. Tudo isso contribui
                        para um maior rendimento e produtividade, com resultados
                        nítidos de imediato.
                    </p>

                    <p>
                        <b>Implantando as mudanças no dia a dia</b>
                    </p>
                    <p>
                        <b
                            >“O importante é encaixar as atividades físicas na
                            sua rotina com consciência e regularidade, evitando
                            exageros. Pode ser com esportes, caminhadas,
                            corridas, academias, o que couber melhor na sua
                            agenda e no seu gosto”, destaca Ricardo Corsi
                            Sanchez.</b

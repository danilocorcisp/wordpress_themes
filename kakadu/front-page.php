            <h1 class="slash-title content__padding-left">
                DE OLHO NO MERCADO
            </h1>
            <article class="article">
                <h2 class="article__title">
                    Área de Gestão do Andbank Brasil aponta tendências da
                    economia e do mercado de investimentos para 2021
                </h2>
               
                <div class="content__text">
                    <p>
                        O ano de 2020 foi de altos e baixos para o mercado de
                        investimentos. Após o choque inicial da pandemia de
                        Covid-19, os mercados oscilaram entre períodos de
                        recuperação e queda, encerrando o ano em alta após o
                        início da vacinação na Europa e a perspectiva de
                        imunização ainda em janeiro no Brasil.
                    </p>

                    <p>
                        Entre os muitos fatos do ano que passou e cenários para
                        o futuro, a avaliação do comportamento do mercado em
                        2020 e as tendências de investimento para 2021 já foram
                        desenhadas. Confira a seguir os principais pontos.
                    </p>

                    <h4>O desempenho da economia em 2020</h4>

                    <p>
                        O ano começou de maneira positiva, porém em março o
                        cenário se inverteu. Em meio à pandemia de Covid-19, o
                        mercado reagiu sobretudo em função das ajudas
                        governamentais, do ambiente da taxa de juros mais baixa
                        e das respostas rápidas do ponto de vista da política
                        econômica dos governos em diferentes países.
                    </p>
                    <p>
                        Tudo isso ajudou a suavizar bastante os efeitos das
                        políticas de quarentena, a única estratégia então
                        disponível. Praticamente todos os mercados tiveram uma
                        retomada em V [forte queda, seguida de acelerada
                        recuperação], com uma economia ainda fragilizada,
                        provavelmente precisando de novos estímulos, porém muito
                        menos em relação ao auge da crise.
                    </p>

                    <h4>Perspectivas do mercado para 2021</h4>

                    <p>
                        Um dos primeiros desafios do ano é lidar com os excessos
                        cometidos em 2020. Será necessário definir como a conta
                        será paga, pois foi preciso gerar muitos gastos para
                        minimizar os impactos da pandemia. As medidas tiveram um
                        impacto positivo, mas que terão de ser de alguma forma
                        compensadas. O grande tema do ano é como pagar a conta
                        dos excessos de 2020. Mas, de forma geral, os mercados devem
                        andar de uma forma razoavelmente positiva.
                    </p>

                    <div class="bg-quotes">
                        <blockquote>
                            “O grande tema do ano é como pagar a conta dos
                            excessos de 2020. Mas, de forma geral, os mercados
                            devem andar de uma forma razoavelmente positiva.”
                        </blockquote>
                    </div>

                    <h4>Novos rumos do mercado de trabalho</h4>

                    <p>
                        A pandemia de Covid-19 evidenciou o problema do
                        desemprego estrutural no Brasil, pois uma das
                        características dessa pandemia foi a elevação da demanda
                        por automação em razão da necessidade de distanciamento
                        social. Parte desse desemprego irá demorar a sumir e se
                        traduz em mais uma das contas que terão de ser pagas ao
                        longo de 2021.
                    </p>

                    <p>
                        A automação de postos de trabalho é um dos frutos do
                        novo ciclo mundial de inovação tecnológica. Além do
                        desemprego transitório, há um desemprego de ordem
                        estrutural. Parte da mão de obra demitida em razão da
                        substituição por máquinas pode ser realocada, porém
                        outra parte demora muito para ser novamente integrada ao
                        mercado de trabalho.
                    </p>

                    <h4>Investimentos em alta em 2021</h4>

                    <p>
                        Ainda há espaço para investimento na renda variável,
                        sobretudo no segmento de commodities no Brasil e em
                        fundos externos. Recentemente, o Andbank teve acesso a
                        fundos espelho [que reproduzem a mesma carteira de
                        outros fundos], cujas estratégias são definidas a nível
                        global, aproveitando a onda positiva do mercado
                        internacional e o ambiente de taxa de juros baixa que
                        ainda deve permanecer.
                    </p>
                    <p>
                        Em relação à renda fixa, o momento é de cautela, pois
                        ainda não está claro qual será o rumo da economia
                        brasileira ao longo de 2021. Com a provável elevação da
                        inflação, é muito possível que haja alguma elevação da
                        taxa de juros para a região de 4% na Selic, gerando
                        ansiedade no mercado de renda fixa.
                    </p>
                </div>
            </article>
